DATAS SEGMENT
   STRING DB 'HELLO, WORLD! ',13,10,'$'
   	RESULT DW ?
   	COUNT DW ?
DATAS ENDS

STACKS SEGMENT
    
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    ;此处输入代码段代码
    MOV CX,SIZEOF STRING
    MOV SI,OFFSET STRING
    XOR BX,BX
    JCXZ DONE
    MOV AL,20H
AGAIN:CMP AL,[SI]
	JNZ NEXT
	INC BX
NEXT: INC SI
	LOOP AGAIN
	
DONE: MOV RESULT,BX
	MOV AX, WORD PTR RESULT
	CLC
	MOV CL,4
	AAM
	ADD AX,3030H
	CMP AH,30H
	JZ JUMP
	PUSH AX
	MOV DL,AH
	MOV AH,2
	INT 21H
	POP AX
JUMP:MOV DL,AL
	MOV AH,2
	INT 21H
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START